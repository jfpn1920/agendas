package logica_y_validaciones;
public class verificar_contacto_favorito {
    public static void main(String[] args) {
        String nombre = "Ana Gómez";
        boolean esFavorito = true;
        if (esFavorito) {
            System.out.println(nombre + " es un contacto favorito.");
        } else {
            System.out.println(nombre + " no es un contacto favorito.");
        }
    }
}